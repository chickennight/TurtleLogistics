<template>
  <div class="SampleGraphContainer">
    <Line :data="chartData" :key="renderCount" :options="chartOptions" />
  </div>
</template>

<script>
import { Line } from "vue-chartjs";
import {
  Chart as ChartJS,
  Title,
  Tooltip,
  Legend,
  BarElement,
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
} from "chart.js";
import { mapState } from "vuex";

ChartJS.register(
  Title,
  Tooltip,
  Legend,
  BarElement,
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement
);

export default {
  name: "SampleGraph",
  components: {
    Line,
  },
  data: () => ({
    chartData: {
      labels: [],
      datasets: [
        {
          label: "주문건수",
          backgroundColor: "salmon",
          borderColor: "salmon",
          color: "red",
          data: [],
        },
      ],
    },
    chartOptions: {
      plugins: {
        legend: {
          display: true,
          labels: {
            color: "white",
          },
        },
      },
      scales: {
        x: {
          ticks: {
            color: "white", // x축 레이블의 글자색을 지정합니다.
          },
        },
        y: {
          ticks: {
            color: "white", // y축 레이블의 글자색을 지정합니다.
          },
        },
      },
    },
    renderCount: 0,
  }),
  mounted() {
    const offset = new Date().getTimezoneOffset() * 60000;
    const today = new Date(Date.now() - offset);
    const end_day = today.toISOString();
    const week = new Date(Date.now() - 6 * 24 * 60 * 60 * 1000 - offset);
    const start_day = week.toISOString();

    const date = {
      end: end_day,
      start: start_day,
    };

    this.$store.dispatch("order/getOrderData", date);

    var idx = 0;
    this.chartData.labels = [];
    this.chartData.datasets.data = [];

    for (let key in this.orderData) {
      this.chartData.labels[idx] = key.substr(4);
      this.chartData.datasets[0].data[idx] = this.orderData[key];
      idx++;
    }

    this.renderCount += 1;
  },
  computed: {
    ...mapState("order", ["orderData"]),
  },
};
</script>

